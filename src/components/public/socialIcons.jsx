// Turn a stored SocialLink value into an href. WhatsApp/phone accept either a full
// URL or a raw number; everything else is treated as a URL.
export function socialHref(platform, value) {
  const v = (value || '').trim();
  if (!v) return null;
  const isUrl = /^https?:\/\//i.test(v);
  if (platform === 'whatsapp') {
    if (isUrl) return v;
    const digits = v.replace(/\D/g, '');
    return digits ? `https://wa.me/${digits}` : null;
  }
  if (platform === 'phone') {
    if (isUrl) return v;
    return `tel:${v.replace(/\s+/g, '')}`;
  }
  return isUrl ? v : `https://${v}`;
}

// The platform shown in the header (prefer WhatsApp, fall back to phone).
export function pickContact(socialLinks = []) {
  return (
    socialLinks.find((l) => l.platform === 'whatsapp') ||
    socialLinks.find((l) => l.platform === 'phone') ||
    null
  );
}
